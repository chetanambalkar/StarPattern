import java.util.Scanner;

public class StarPattern {
    public static void main() {
        Scanner sc = new Scanner (System.in);
        int n = nextInt ();
        int m = nextInt ();
        for (i=0; i<=n; i++) {
            for (j=0; j<=m; j++)
         {
            System.out.println("*");
         }
        }
    }
}
